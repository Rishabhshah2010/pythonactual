# equals
a = 5

# plus equals
a += 2  # is shorthand for a = a + 2
print(a)

# minus equals
a -= 3  # shorthand for a = a-3
print(a)

# *=
a *= 3 # a = a * 3
print(a)

# /=
a /= 3  # a = a/3
print(a)

# **=
a **= 2  # a = a ** 2
print(a)


# functions
# divmod()
a,b = divmod(14,6)
print(a,b)

# pow()
print(pow(5, 9))

# sum()
print(sum([1,2,5,6,7,7,9]))

# max()
print(max([1,2,4,7,8,9]))

# min()
print(min([1,2,3,4,5,6,7]))

# round()
d = 6.789638738
print(round(d, 3))